public class TbContent {
    private String idFile;
    private String nmOrgFile;

    private String boSaveFile;

    private String nmFileExt;
    private String daSave;
    private int cnHit;

    public String getIdFile() {
        return idFile;
    }

    public String getNmOrgFile() {
        return nmOrgFile;
    }

    public String getBoSaveFile() {
        return boSaveFile;
    }

    public String getNmFileExt() {
        return nmFileExt;
    }

    public String getDaSave() {
        return daSave;
    }

    public int getCnHit() {
        return cnHit;
    }

    public void setIdFile(String idFile) {
        this.idFile = idFile;
    }

    public void setNmOrgFile(String nmOrgFile) {
        this.nmOrgFile = nmOrgFile;
    }

    public void setBoSaveFile(String boSaveFile) {
        this.boSaveFile = boSaveFile;
    }

    public void setNmFileExt(String nmFileExt) {
        this.nmFileExt = nmFileExt;
    }

    public void setDaSave(String daSave) {
        this.daSave = daSave;
    }

    public void setCnHit(int cnHit) {
        this.cnHit = cnHit;
    }
}
